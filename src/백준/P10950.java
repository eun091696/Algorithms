package 백준;

import java.util.Scanner;

public class P10950 {

	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		
		int t, A, B = 0;
		
		t = in.nextInt();
		
		for(int i = 0; i < t; i++) {
			A = in.nextInt();
			B = in.nextInt();
			
			System.out.println(A + B);
		}
	
	}

}
